def number_compare(a, b):
    """Report on whether a>b, b>a, or b==a"""
    if a == b:
        print("Numbers are equal")
    elif a < b:
        print("Second is greater")
    elif a > b:
        print("First is greater")    
    
    

number_compare(1, 1)
number_compare(-1, 1)
 
        
